/*---------------------------------------------------------------
-                         POST-ROUTES
---------------------------------------------------------------*/

const sequelize = require('../../config/connection');
const router = require('express').Router();
const { Post } = require('../../models');

/*---------------------------------------------------------------
-                         GET /api/posts
---------------------------------------------------------------*/

/*------------------------   GET ALL     ----------------------*/

router.get('/', (req, res) => {
  console.log('======================');
  Post.findAll({
    order: [['created_at', 'DESC']]
  })
  .then(dbPostData => res.json(dbPostData))
  .catch(err => {
    console.log(err);
    res.status(500).json(err);
  });
});

/*------------------------   GET 1     -----------------------*/

router.get('/:id', (req, res) => {
  Post.findOne({
    where: {
      id: req.params.id
    },
  })
    .then(dbPostData => {
      if (!dbPostData) {
        res.status(404).json({ message: 'No post found with this id' });
        return;
      }
      res.json(dbPostData);
    })
    .catch(err => {
      console.log(err);
      res.status(500).json(err);
    });
});

/*---------------------------------------------------------------
-                         CREATE POSTS /api/posts
---------------------------------------------------------------*/

router.post('/', (req, res) => {
  Post.create({
    title: req.body.title,
    post_url: req.body.post_url,
    user_id: req.body.user_id
  })
  .then(dbPostData => res.json(dbPostData))
  .catch(err => {
    console.log(err);
    res.status(500).json(err);
  });
});

/*---------------------------------------------------------------
-                         UPDATE POSTS /api/posts
---------------------------------------------------------------*/

router.put('/:id', (req, res) => {
  Post.update(
    {
      title: req.body.title
    },
    {
      where: {
        id: req.params.id
      }
    }
  )
    .then(dbPostData => {
      if (!dbPostData) {
        res.status(404).json({ message: 'No post found with this id' });
        return;
      }
      res.json(dbPostData);
    })
    .catch(err => {
      console.log(err);
      res.status(500).json(err);
    });
});

/*---------------------------------------------------------------
-                         DELETE POSTS /api/posts
---------------------------------------------------------------*/

router.delete('/:id', (req, res) => {
  Post.destroy({
    where: {
      id: req.params.id
    }
  })
    .then(dbPostData => {
      if (!dbPostData) {
        res.status(404).json({ message: 'No post found with this id' });
        return;
      }
      res.json(dbPostData);
    })
    .catch(err => {
      console.log(err);
      res.status(500).json(err);
    });
});

module.exports = router;
